# Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.

numero1 = int(input('Digite um número: '))
dobro = numero1 ** 2
triplo = numero1 ** 3
raizQuadrada = numero1 ** (1/2)

print(f'O dobro de {numero1} é {dobro} e o triplo de {numero1} é {triplo} e a raiz quadrada de {numero1} é {raizQuadrada}')